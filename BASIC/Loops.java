package in.com.raystec.loops;

public class Loops {

	public static void main(String[] args) {
		//for loop
		for(int shot = 0; shot <=5; shot++) {
			System.out.println(shot+1 + "shot" );
		}
		System.out.println(" \n");
		//while loop
		int i=1;
		while(i<=5) {
			System.out.println(i + "shot");
			i++;
		}
		System.out.println(" \n");
		//do while
		int j=0;
		do {
			System.out.println(j + "shot");
			j++;
			}while(j<=5);
		System.out.println(" \n");
	//print 8 
		for(i = 0; i <=5; i++) {
			System.out.println("8");
		}
	}
	

}
