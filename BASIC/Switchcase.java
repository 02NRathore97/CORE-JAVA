package in.com.raystec.String;

public class Switchcase {
	public static void main(String[] args) {
		char s = 'i';
	switch(s){
		case 'a':
			System.out.println("vowel");
			break;
		case 'e':
			System.out.println("vowel");
			break;
		case 'i':
			System.out.println("vowel");
			break;
		case 'o':
			System.out.println("vowel");
			break;
		case 'u':
			System.out.println("vowel");
			break;
			default:
				System.out.println("consonant");
			
		}
	}
}
