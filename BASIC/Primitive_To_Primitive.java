package in.com.raystec.conversions;
//typecasting  = converting one data_type into another
public class Primitive_To_Primitive {
public static void main(String[] args) {
	float result ;
	int i = 24;
	int j = 5;
	result = i/j; //output will be interger
System.out.println(result);

//typecasting
result = (float)i/j;  //output will be floats
System.out.println(result);
	
	
	
}
}
